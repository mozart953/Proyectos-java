    public void Calcular() {

        try {
            limpiarMatriz();
            Tsllegada = Double.parseDouble(tasaLlegada.getText());
            TsServicio = Double.parseDouble(tasaServicio.getText());
            calculoTiempo();
            calculoTiempo2();

            //Wq1 = calcular.CalcularWq(TsServicio, Tsllegada);
            //Ws1 = calcular.CalcularWs(Wq1, TsServicio);
            Wq1();
            Ws1();
            Ls1();
            Lq1();
            //Ls1 = calcular.CalcularLs(Tsllegada, Ws1);
            //Lq1 = calcular.CalcularLq(Tsllegada, Wq1);

            Wq.setText(String.valueOf(Wq1));
            Ws.setText(String.valueOf(Ws1));
            Ls.setText(String.valueOf(Ls1));
            Lq.setText(String.valueOf(Lq1));

            P1 = calcular.CalculaP(TsServicio, Tsllegada);
            p0 = calcular.CalculaOcio(P1);

            P.setText(String.valueOf(P1));
            Po.setText(String.valueOf(p0));

            confirmar = n1.getText();

            if (!"".equals(confirmar)) {
                n = Integer.parseInt(n1.getText());
                Pn = new double[n + 1];
                Pn = calcular.calculoPn(p0, P1, n);
                LlenarMatriz(Pn, n);
                DibujarProbabilidad(Pn, n);
            }

            if (!"".equals(t1.getText())) {
                tiempo = Integer.parseInt(t1.getText());
                tsL = new double[tiempo + 1];
                tsS = new double[tiempo + 1];
                tsL = calcular.TasaLl(Tsllegada, tiempo);
                tsS = calcular.TasaSer(TsServicio, tiempo);

                DibujarTasaLllegada(tsL, tiempo);
                DibujarTasaServicio(tsS, tiempo);

                tiempoMinutos1 = calcular.Wqt(TsServicio, P1, tiempo);
                tiempoMinutos2 = calcular.Wst(TsServicio, P1, tiempo);
                Po1.setText(String.valueOf(tiempoMinutos1));
                Po2.setText(String.valueOf(tiempoMinutos2));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Los datos ingresados deben ser numericos decimales.");
        }

    }

    public void LlenarMatriz(double pn[], int n) {
        for (int ctn = 0; ctn < n + 1; ctn++) {
            modTabM.addRow(new Object[]{
                String.valueOf(ctn), String.valueOf(pn[ctn])
            });
        }
    }

    private void limpiarMatriz() {
        int filas = modTabM.getRowCount();
        for (int a = 0; a < filas; a++) {
            modTabM.removeRow(0);
        }
    }

    public void calculoTiempo() {
        if (Min.isSelected()) {
            Tsllegada = Tsllegada / 60;
        } else if (Sec.isSelected()) {
            Tsllegada = Tsllegada / 3600;
        }

    }

    public void calculoTiempo2() {
        if (Min2.isSelected()) {
            TsServicio = TsServicio / 60;
        } else if (Sec2.isSelected()) {
            TsServicio = TsServicio / 3600;
        }

    }
    
