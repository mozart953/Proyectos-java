    public void DescomponerPregunta() {
        String pregunta = "";
        pregunta = text1.getText() + " ";
        String acumulador = "";
        String auxiliar = "";
        String auxiliar2 = "";
        String auxiliar3 = "";

        InicializarNombres();
        InicializarProgenitor();

        for (int a = 0; a < pregunta.length(); a++) {
            auxiliar += pregunta.charAt(a);
            if (!" ".equals(auxiliar) && !"¿".equals(auxiliar) && !"?".equals(auxiliar)) {
                acumulador += auxiliar;
                auxiliar = "";
                //System.out.println(acumulador);
            } else {
                auxiliar = "";
                System.out.println(acumulador);
                for (int b = 0; b < diccionario.length; b++) {
                    auxiliar2 = diccionario[b];
                    if (acumulador.equals(auxiliar2)) {
                        if (diccionario[b].equals("quien") || diccionario[b].equals("QUIEN") || diccionario[b].equals("Quién")
                                || diccionario[b].equals("quienes")) {
                            contador++;
                            System.out.println(acumulador);
                            //acumulador = "";
                        } else {
                            contador++;
                            System.out.println(acumulador);
                        }
                        //System.out.println(acumulador);
                        acumulador = "";

                    }

                }

                for (int c = 0; c < diccionarioNombres.length; c++) {
                    auxiliar3 = diccionarioNombres[c];
                    if (acumulador.equals(auxiliar3)) {
                        Nombre = acumulador;
                        acumulador = "";
                    }

                }
                acumulador = "";
            }
            //auxiliar = "";
        }
        //System.out.println(acumulador);
        System.out.println(contador + " " + Nombre);

    }

