    private void boton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton1ActionPerformed
        try {
            Poblacion = Integer.parseInt(N1.getText());
            Muestra = Integer.parseInt(n11.getText());
            
            if(check1.isSelected()){
                p1.setEnabled(false);
                k1.setEnabled(true);
                k = Integer.parseInt(k1.getText());
                decidir2();
                
            }else{
                p1.setEnabled(true);
                k1.setEnabled(false); 
                probabilidad = Double.parseDouble(p1.getText());
                decidir1();
               
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ha ocurrido un error, verifique que las entradas sean correctas.");
        }
    }//GEN-LAST:event_boton1ActionPerformed

