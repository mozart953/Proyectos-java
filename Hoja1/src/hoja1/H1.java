    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cad = this.txt.getText();
        int contador = 0;
        char vocal1 = 'a';
        char vocal2 = 'e';
        char vocal3 = 'i';
        char vocal4 = 'o';
        char vocal5 = 'u';
        
        for (int i = 0; i < cad.length(); i++) {
            if (cad.charAt(i) == vocal1) {
                contador++;
            }
            if (cad.charAt(i) == vocal2) {
                contador++;
            }
            if (cad.charAt(i) == vocal3) {
                contador++;
            }
            if (cad.charAt(i) == vocal4) {
                contador++;
            }
            if (cad.charAt(i) == vocal5) {
                contador++;
            }
        }
        
        if (contador >= 0) {
            JOptionPane.showMessageDialog(rootPane, "El número de vocales es: " + contador);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String palabra = this.txt.getText();
        String palabrainvertida = "";
        for (int n = palabra.length() - 1; n >= 0; n--) {
            palabrainvertida = palabrainvertida + palabra.charAt(n);
            
        }
        JOptionPane.showMessageDialog(rootPane, palabrainvertida);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String palabra2 = this.txt.getText();
        String caracter = this.car.getText();
        int contador = 0;
        for (int i = 0; i < palabra2.length(); i++) {
            if (palabra2.charAt(i) == caracter.charAt(0)) {
                contador++;
            }
            
        }
        if (contador >= 0) {
            JOptionPane.showMessageDialog(rootPane, "El caracter " + caracter + " se repite " + contador + " veces en la palabra "
                    + palabra2);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

