    public void DibujarTasaLllegada(double valoresProb[], double tiempo) {
        DefaultCategoryDataset dtsc = new DefaultCategoryDataset();
        for (int re = 0; re <= tiempo; re++) {
            dtsc.setValue(valoresProb[re], "Probabilidad", String.valueOf(re));
        }
        JFreeChart grafica = ChartFactory.createBarChart3D("Tasa de Llegada", "Tiempo", "No. de llegadas", dtsc, PlotOrientation.VERTICAL, true, true, false);

        ChartPanel pa = new ChartPanel(grafica);
        /*add(pa);
        pa.setBounds(0, 50, 600, 300);
        panel.add(pa, BorderLayout.NORTH);*/
        pa.setMouseWheelEnabled(true);
        pa.setPreferredSize(new Dimension(440, 240));
        panel1.setLayout(new BorderLayout());
        panel1.add(pa, BorderLayout.NORTH);
        pack();
        //repaint();

    }

