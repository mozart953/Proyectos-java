      Crearusuar modifi = new Crearusuar(); 
        
        String pin1 = this.pin.getText();
        String regi [] = new String [7];
        String descripcion [] = new String [7];
        descripcion [0] = "Nombre:";
        descripcion [1] = "Tarjeta:";
        descripcion [2] = "pin:";
        descripcion [3] = "Monto:";
        descripcion [4] = "Saldo:";
        descripcion [5] = "Retiro:";
        descripcion [6] = "Deposito:";
        
        if(pin1.length()==4){
            regi=modifi.Leerusuario1(pin1);
                if(regi[0]=="-1"){
                    JOptionPane.showMessageDialog(rootPane, "Usuario no registrado");
                }
                else if(regi[0]=="-2"){
                    JOptionPane.showMessageDialog(rootPane, "Error desconocido");
                }
                else if(regi[0]=="-3"){
                    JOptionPane.showMessageDialog(rootPane, "El archivo no existe");
                }
                else{
                    for(int x=5;x<7;x++){
                        String ord[]= {descripcion[x],regi[x]};
                        modelo1.addRow(ord);
                    }
                }
        
        }else{
            JOptionPane.showMessageDialog(rootPane, "El pi debe tener 4 caracteres numericos");
        }
          
        
      
