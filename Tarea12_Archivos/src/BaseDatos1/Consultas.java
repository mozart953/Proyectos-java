    private void Tabla() throws ClassNotFoundException{
        DefaultTableModel modeloTabla = (DefaultTableModel) tabla1.getModel();
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsm;
        int columnas;
        try{
            Connection cn = Conectar.conexion();
            //Id_Articulos,Nombre,Precio,Id_CodigoF
            ps = cn.prepareStatement("Select * from dbo.funcion2(2)");
            rs= ps.executeQuery();
            rsm = rs.getMetaData();
            columnas = rsm.getColumnCount();
            while(rs.next()){
                Object [] fila = new Object[columnas];
                for(int i=0;i<columnas;i++){
                    fila [i] = rs.getObject(i+1);
                }
                modeloTabla.addRow(fila);
            }
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
    
    
