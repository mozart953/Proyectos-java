    public void decidir2 (){
        double Prob = (double)k/Poblacion;
        double np = Prob *  Muestra;
        panelControl panel = new panelControl();
        
        if((Prob<0.10) && (np<10)){
            this.setVisible(false);
            po.setVisible(true);
            po.n1.setText(n11.getText());
            po.p1.setText(String.valueOf(Prob));
            JOptionPane.showMessageDialog(rootPane, "Distribucion de Poisson");
        }
        else {
            this.setVisible(false);
            panel.setVisible(true);
            panel.poblacion.setText(N1.getText());
            panel.n.setText(n11.getText());
            panel.K1.setText(k1.getText());
            JOptionPane.showMessageDialog(rootPane, "Distribucion de Hipergeométrica");
            
        }
    
    }
    
    public void decidir1(){
        double np = probabilidad *  Muestra;
        panelControl panel = new panelControl();
        
        if((probabilidad<0.10) && (np<10)){
             this.setVisible(false);
             po.setVisible(true);
             po.n1.setText(n11.getText());
             po.p1.setText(p1.getText());
            JOptionPane.showMessageDialog(rootPane, "Distribucion de Poisson");
        }
        else {
            this.setVisible(false);
            panel.setVisible(true);
            panel.poblacion.setText(N1.getText());
            panel.n.setText(n11.getText());
            panel.p.setText(p1.getText());
            JOptionPane.showMessageDialog(rootPane, "Distribucion de Binomial");            
        }
    }
    
