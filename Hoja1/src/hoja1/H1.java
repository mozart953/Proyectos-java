    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String dato1 = this.txt.getText();
        String dato2 = this.car.getText();
        int data = dato2.length();
        int data2 = dato1.length();
        int res = data2 - data;
        int a = 0;
        int b = 0;
   
        while (res >= 0) {
            if (dato2.equals(dato1.substring(res, data2))) {
                a = res;
                b = data2;
            }
            res--;
            data2--;
        }
        
        String cap = dato1.substring(a, b).toUpperCase();
        
        if (a == 0 || b == dato1.length()) {            
            if (a == 0) {
                JOptionPane.showMessageDialog(rootPane, cap + dato1.substring(b, dato1.length()));
            }
            if (b == dato1.length()) {
                JOptionPane.showMessageDialog(rootPane, dato1.substring(0, a) + cap);                
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, dato1.substring(0, a) + cap + dato1.substring(b, dato1.length()));
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

